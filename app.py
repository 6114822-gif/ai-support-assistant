@app.route("/api/chat", methods=["POST"])
def chat():
    if client is None:
        return jsonify({"error": "Не настроен ANTHROPIC_API_KEY на сервере."}), 500

    data = request.get_json(force=True)
    history = data.get("history", [])

    if not history:
        return jsonify({"error": "Пустой запрос."}), 400

    is_first_message = len(history) <= 1
    contact_already_shown = any(
        msg.get("role") == "assistant" and "support@techstore.example" in msg.get("content", "")
        for msg in history
    )

    try:
        response = client.messages.create(
            model=MODEL,
            max_tokens=MAX_TOKENS,
            system=build_system_prompt(is_first_message, contact_already_shown),
            messages=history,
        )
    except anthropic.APIError as e:
        return jsonify(
            {"error": f"Ошибка обращения к Claude API: {e}. Проверь ANTHROPIC_API_KEY в .env."}
        ), 502
    except Exception as e:
        return jsonify({"error": f"Не удалось получить ответ: {e}"}), 500

    text_blocks = [block.text for block in response.content if block.type == "text"]
    reply_text = "\n".join(text_blocks) if text_blocks else "(пустой ответ)"

    return jsonify({"reply": reply_text})


